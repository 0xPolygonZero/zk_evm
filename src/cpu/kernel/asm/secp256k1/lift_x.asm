%macro secp_lift_x
    // stack: x, v
    DUP1
    // stack: x, x, v
    %cubemodn_secp
    // stack: x^3, x, v
    PUSH 7
    // stack: 7, x^3, x, v
    %addmodn_secp
    // stack: x^3+7, x, v
    %sqrt_secp
    // stack: y, x, v
    DUP1
    // stack: y, y, x, v
    PUSH 1
    // stack: 1, y, y, x, v
    AND
    // stack: 1 & y, y, x, v
    PUSH 27
    // stack: 27, 1 & y, y, x, v
    DUP5
    // stack: v, 27, 1 & y, y, x, v
    SUB
    // stack: v - 27, 1 & y, y, x, v
    EQ
    // stack: correctParity, y, x, v
    DUP2
    // stack: y, correctParity, y, x, v
    %secp_base
    // stack: N, y, correctParity, y, x, v
    SUB
    // stack: N - y, correctParity, y, x, v
    SWAP1
    // stack: correctParity, N - y, y, x, v
    %select_bool
    // stack: goody, x, v
    SWAP2
    // stack: v, x, goody
    POP
    // stack: x, goody
%endmacro

%macro cubemodn_secp
    // stack: x
    DUP1
    // stack: x, x
    %squaremodn_secp
    // stack: x^2, x
    %mulmodn_secp
%endmacro

%macro addmodn_secp
    // stack: x, y
    %secp_base
    // stack: N, x, y
    SWAP2
    // stack: y, x, N
    ADDMOD
%endmacro

